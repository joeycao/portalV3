local _M = {}
_M._VERSION = '1.0'
local mt = { __index = _M }
local log = require "portal.common.log"
local json_util = require "portal.common.json_util"

--
-- return errors container and has_err.
--
function _M.valid_by_regex(pattern,text,error_message,errors)
  text = text or ""
  errors = errors or {}
  local has_err = false
  if ngx.re.match(text, pattern) == nil then
    has_err = true
    table.insert(errors,error_message)
  end
  return errors,has_err
end
--
-- return errors container and has_err.
--
function  _M.vaild_json_text(text,error_message,errors)
  errors = errors or {}
  local data = json_util.decode(text)
  local has_err = false
  if(data == nil) then
    has_err = true
    table.insert(errors,error_message)
  end
  return errors,has_err
end

function  _M.vaild_id(text,error_message,errors)
  return  _M.valid_by_regex("[a-zA-Z0-9]{1,50}",text,error_message,errors)
end

function  _M.vaild_en_name(text,error_message,errors)
  return  _M.valid_by_regex("[a-zA-Z0-9\\._-]{1,100}",text,error_message,errors)
end

function  _M.vaild_number(text,error_message,errors)
  return  _M.valid_by_regex("[0-9]+",text,error_message,errors)
end

function _M.vaild_filename(text,error_message,errors)
  return  _M.valid_by_regex("[^/\\\\]+",text,error_message,errors)
